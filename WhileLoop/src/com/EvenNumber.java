public class EvenNumber {
	public static void main(String args[]) {
	int num=10;
	while(num>=0) {
		System.out.print(num+" ");
		num=num-2;
	}
	
	System.out.println("---------------");
	
	int num1=10;
	do{
		System.out.print(num1+" ");
		num1=num1-2;
	}while(num1>=0);
		
	
	
	System.out.println("---------------");
	
	
	for(int i=10;i>=0;i--)
	{ 
		if(i%2==0) {
			System.out.print(i+" ");
		}
	}
	}
	
	}
	
